package personnel;

import java.io.Serializable;
import java.time.LocalDate;
import java.util.TreeSet;
import java.util.SortedSet;


/**
 * Employé d'une ligue hébergée par la M2L. Certains peuvent 
 * être administrateurs des employés de leur ligue.
 * Un seul employé, rattaché à  aucune ligue, est le root.
 * Il est impossible d'instancier directement un employé, 
 * il faut passer la méthode {@link Ligue#addEmploye addEmploye}.
 */

public class Employe implements Serializable, Comparable<Employe>
{
	private static final long serialVersionUID = 4795721718037994734L;
	private String nom, prenom, password, mail;
	private Ligue ligue;
	private SortedSet<Employe> employes; //itération 3
	private GestionPersonnel gestionPersonnel;
	private LocalDate dateDepart, dateArrivee;
	private int id;
	
	Employe(GestionPersonnel gestionPersonnel, Ligue ligue, String nom, String prenom, String mail, String password, LocalDate dateArrivee, LocalDate dateDepart)
	{
		this.gestionPersonnel = gestionPersonnel;
		this.nom = nom;
		this.prenom = prenom;
		this.password = password;
		this.mail = mail;
		this.ligue = ligue;
		this.dateArrivee = dateArrivee;  //Itération 1
		this.dateDepart = dateDepart;  //Itération 1
	
	}

	public Employe (GestionPersonnel gestionPersonnel, int id, String nom) //itération 3
		{
			this.nom = nom;
			employes = new TreeSet<>();
			this.gestionPersonnel = gestionPersonnel;
	//		administrateur = gestionPersonnel.getRoot();
			this.id = id;
		}

	/**
	 * Retourne vrai si l'employé est administrateur de la ligue 
	 * passée en paramètre.
	 * @return vrai si l'employé est administrateur de la ligue 
	 * passée en paramètre.
	 * @param ligue la ligue pour laquelle on souhaite vérifier si this 
	 * est l'admininstrateur.
	 */
	
	public boolean estAdmin(Ligue ligue)
	{
		return ligue.getAdministrateur() == this;
	}
	
	/**
	 * Retourne vrai si l'employé est le root.
	 * @return vrai si l'employé est le root.
	 */
	
	public boolean estRoot()
	{
		return gestionPersonnel.getRoot() == this;
	}
	
	/**
	 * Retourne le nom de l'employé.
	 * @return le nom de l'employé. 
	 */
	
	public String getNom()
	{
		return nom;
	}

	/**
	 * Change le nom de l'employé.
	 * @param nom le nouveau nom.
	 */
	
	public void setNom(String nom) throws SauvegardeImpossible
	{
		this.nom = nom;
		gestionPersonnel.update(this);
	}

	/**
	 * Retourne le prénom de l'employé.
	 * @return le prénom de l'employé.
	 */
	
	public String getPrenom()
	{
		return prenom;
	}
	
	/**
	 * Change le prénom de l'employé.
	 * @param prénom le nouveau prénom de l'employé. 
	 */

	public void setPrenom(String prenom) throws SauvegardeImpossible
	{
		this.prenom = prenom;
		gestionPersonnel.update(this);
	}

	/**
	 * Retourne le mail de l'employé.
	 * @return le mail de l'employé.
	 */
	
	public String getMail()
	{
		return mail;
	}
	
	/**
	 * Change le mail de l'employé.
	 * @param mail le nouveau mail de l'employé.
	 */

	public void setMail(String mail) throws SauvegardeImpossible  
	{
		this.mail = mail;
		gestionPersonnel.update(this);
	}
	
	public LocalDate getDateArrivee() {  //Getter pour la date d'arrivée
		return dateArrivee;
	}
	
	public void setDateArrivee(LocalDate dateArrivee) throws DateImpossible{  //Setter pour la date d'arrivée
		if(dateArrivee != null && dateDepart != null &&  dateDepart.isBefore(dateArrivee)) throw new DateImpossible();
		this.dateArrivee = dateArrivee;
	}
	
	public LocalDate getDateDepart() {  //Getter pour la date de départ
		return dateDepart;
	}
	
	public void setDateDepart(LocalDate dateDepart) throws DateImpossible{  //Setter pour la date de départ
		if(dateArrivee != null && dateDepart != null &&  dateArrivee.isAfter(dateDepart)) throw new DateImpossible();
		this.dateDepart = dateDepart;
	}
	
	/**
	 * Retourne vrai si le password passé en paramètre est bien celui
	 * de l'employé.
	 * @return vrai si le password passé en paramètre est bien celui
	 * de l'employé.
	 * @param password le password auquel comparer celui de l'employé.
	 */
	
	public String getPassword()
	{
		return password;
	}

	public boolean checkPassword(String password)
	{
		return this.password.equals(password);
	}

	/**
	 * Change le password de l'employé.
	 * @param password le nouveau password de l'employé. 
	 * @throws SauvegardeImpossible 
	 */
	
	public void setPassword(String password) throws SauvegardeImpossible
	{
		this.password= password;
		gestionPersonnel.update(this);
	}

	/**
	 * Retourne la ligue à laquelle l'employé est affectée.
	 * @return la ligue à laquelle l'employé est affectée.
	 */
	
	public Ligue getLigue()
	{
		return ligue;
	}

	/**
	 * Supprime l'employée. Si celui-ci est un administrateur, le root
	 * récupère les droits d'administration sur sa ligue.
	 */
	
	public void remove() throws SauvegardeImpossible
	{
		Employe root = gestionPersonnel.getRoot();
		if (this != root)
		{
			if (estAdmin(getLigue()))
				getLigue().setAdministrateur(root);
			getLigue().remove(this);
		}
		else
			throw new ImpossibleDeSupprimerRoot();
	}

	@Override
	public int compareTo(Employe autre)
	{
		int cmp = getNom().compareTo(autre.getNom());
		if (cmp != 0)
			return cmp;
		return getPrenom().compareTo(autre.getPrenom());
	}
	
	@Override
	public String toString()
	{
		String res = nom + " " + prenom + " " + mail + " " + dateArrivee + " " + dateDepart + " (" ;
		if (estRoot())
			res += "super-utilisateur";
		else
			res += ligue.toString();
		return res + ")";
	}
	
	public int getId() { //itération 3
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public void update(String string) throws SauvegardeImpossible {
		 gestionPersonnel.update(this);
	}
}
